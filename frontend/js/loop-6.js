for (let count1 = 1; count1 <= 10; count1++){
    document.write(`${count1}<br>`)
}
for (let count2 = 10; count2 > 0; count2--){
    document.write(`${count2}<br>`)
}
for (let count3 = 10; count3 > 0; count3--){
    if (count3 % 2 == 0){
        document.write(`${count3}<br>`)
    }
}
for (let count4 = 1; count4 <= 100; count4++){
    if (count4 % 3 == 0){
        document.write(`${count4}<br>`)
    }
}