let maxNum = Math.max(30, 60, 5)
console.log(`30, 60, 5 중 최대 값은 ${maxNum} 입니다.`)

let minNum = Math.min(8, 10, 15)
console.log(`8, 10, 15 중 최소 값은 ${minNum} 입니다.`)

let roundNum = Math.round(3.3)
console.log(`3.3를 소수점 첫째 자리에서 반올림한 값은 ${roundNum}입니다.`)

let ceilNum = Math.ceil(3.3)
console.log(`3.3를 소수점 첫째 자리에서 올림한 값은 ${ceilNum}입니다.`)

let floorNum = Math.floor(3.3)
console.log(`3.3를 소수점 첫째 자리에서 버림한 값은 ${floorNum}입니다.`)

let absNum = Math.abs(-10)
console.log(`-10의 절대 값은 ${absNum}입니다.`)

let ranNum = Math.random()
console.log(`0에서 1사이의 숫자 중 랜덤 값은 ${ranNum}입니다.`)