const A = 10
const B = 3

const add = A + B
console.log(`A 더하기 B는 ${add} 입니다.`)

const sub = A - B
console.log(`A 빼기 B는 ${sub} 입니다.`)

const mul = A * B
console.log(`A 곱하기 B는 ${mul} 입니다.`)

const div = A / B
console.log(`A 나누기 B는 ${div} 입니다.`)

const mod = A % B
console.log(`A 나누기 B의 나머지는 ${mod} 입니다.`)

const exp = A ** B
console.log(`A의 B제곱은 ${exp} 입니다.`)